public class Pasien {
    private String kodePasien;
    private String namaPasien;
    private String alamatPasien;
    private String tanggalLahir;
    private String kepalaKeluarga;
    private String jenisKelamin;
    private String noTelepon;
    
    // Constructor
    public Pasien() {}
    
    // Getter dan Setter
    public String getKodePasien() { return kodePasien; }
    public void setKodePasien(String kodePasien) { this.kodePasien = kodePasien; }
    
    public String getNamaPasien() { return namaPasien; }
    public void setNamaPasien(String namaPasien) { this.namaPasien = namaPasien; }
    
    public String getAlamatPasien() { return alamatPasien; }
    public void setAlamatPasien(String alamatPasien) { this.alamatPasien = alamatPasien; }
    
    public String getTanggalLahir() { return tanggalLahir; }
    public void setTanggalLahir(String tanggalLahir) { this.tanggalLahir = tanggalLahir; }
    
    public String getKepalaKeluarga() { return kepalaKeluarga; }
    public void setKepalaKeluarga(String kepalaKeluarga) { this.kepalaKeluarga = kepalaKeluarga; }
    
    public String getJenisKelamin() { return jenisKelamin; }
    public void setJenisKelamin(String jenisKelamin) { this.jenisKelamin = jenisKelamin; }
    
    public String getNoTelepon() { return noTelepon; }
    public void setNoTelepon(String noTelepon) { this.noTelepon = noTelepon; 
    }
}

